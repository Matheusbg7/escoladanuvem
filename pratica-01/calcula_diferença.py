# Esse código tem como objetivo ler valores inteiros inputados pelo usuario

# Ler os valores inteiros fornecidos pelo usuario
A = int(input("Digite o valor de A: "))
B = int(input("Digite o valor de B: "))
C = int(input("Digite o valor de C: "))
D = int(input("Digite o valor de D: "))

# Calculo da diferença 
DIFERENCA = (A * B) - (C * D)


# Exibir os resultados
print("A formula para a diferença é: DIFERENCA (A * B) - (C * D)")
print(f"Substituindo os valores fornecidos: DIFERENCA ({A} * {B}) - ({C} * {D})")
print(f"O resultado da diferença é: DIFERENCA = {DIFERENCA}")
